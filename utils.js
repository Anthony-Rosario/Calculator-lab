//Addition Function
export function getAddSum(randomNumber, randomNumber2) {
    return randomNumber + randomNumber2;
}



//Subtraction Function
export function getSubSum(randomNumber, randomNumber2) {
    return randomNumber - randomNumber2;
}



//Division Function
export function getDivSum(randomNumber, randomNumber2) {
    return randomNumber / randomNumber2;
}



//Multiplication Function
export function getMulSum(randomNumber, randomNumber2) {
    return randomNumber * randomNumber2;
}